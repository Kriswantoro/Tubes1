    <p align="text-center"> Anime ( dibaca A-NI-ME, bukan A-NIM ) adalah film animasi khas jepang.
      *Lalu apa bedanya dengan film Animasi dari negara lain? ( mungkin pertanyaan itu akan muncul untuk yg baru mengenal anime). sebenarnya tidak terlalu berbeda dengan film animasi dari negara-negara lain, karena sama-sama dibuat dengan menggunakan objek/gambar yg kemudian digerakan. rekaman gerakan dari objek inilah yg disebut animasi atau biasa kita kenal dengan kartun.
      Yang membedakanya adalah Anime mempunyai ciri khas sendiri. jika biasanya cerita dalam film kartun akan habis dalam satu episode ( meskipn memiliki banyak episode tetapi disetiap episode akan memiliki cerita yg bebeda ), tetapi tidak dengan Anime. Anime akan memiliki cerita yg panjang sehingga akan dibuat sampai beberapa episode layaknya sinetron. dan yg membuat Anime menarik adalah cara penggambaran karakter yg ditampilkan benar-benar mirip dengan makhluk hidup terutama manusia. mulai dari kepala hingga kaki ditampilkan secara detail sehingga membuatnya terlihat lebih hidup, dan cerita yg ada dalam anime lebih bervariasi. mulai dari cerita zaman dulu / Legenda, sampai masa depan ( biasanya bercerita tentang teknologi ). Anime juga menggambarkan berbagai aktifitas di negara jepang, mulai dari agama, budaya, dll.</p>

